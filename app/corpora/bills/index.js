// app/corpora/bills/index.js
// Bills = Indian Parliament's bill corpus.
// ~9,900 bills across 1952-2026 from sansad.in's getBills API,
// daily-mirrored via parliamentwatch-data's build_bills_sansad.py.
//
// Third registered corpus under SansadSaar (after DRSC and CAG).
// Pipeline-validation milestone: this module was the first added without
// fresh shell architecture — just slot it in. If something cracked here
// the v1.0a corpus-module shell would need a rethink before adding
// Hansard or Questions.
//
// Independence Principle: this file does not import from drsc/index.js
// or cag/index.js. Schema-shape helpers, parseQuery, etc., are duplicated
// from the CAG module by design. Refactor when a fourth corpus arrives
// and the duplication starts to bite.
//
// Sharded fetch: Bills uses index-meta.json + index-<NN>.json shards
// (~1 MB each), differing from DRSC's flat reports.json + CAG's
// reports.json. App fetches index-meta.json first, then all shards in
// parallel. Pre-emptive sharding decided 2026-05-10 — full archive is
// 10.5 MB single-file today (well under CF's 25 MiB cap), but PRS
// payload merge will grow it and changing on-disk shape after the app
// depends on it costs more than starting sharded.


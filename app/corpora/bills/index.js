// ── JS API surface ─────────────────────────────────────────────────────────

const api = {
  list({ status, billType, billCategory, billYear, billIntroducedInHouse } = {}) {
    return getAllBills().filter(b => {
      if (status && b.status !== status) return false;
      if (billType && b.billType !== billType) return false;
      if (billCategory && b.billCategory !== billCategory) return false;
      if (billYear != null && String(b.billYear) !== String(billYear)) return false;
      if (billIntroducedInHouse && b.billIntroducedInHouse !== billIntroducedInHouse) return false;
      return true;
    });
  },
  get(key) {
    return getAllBills().find(b => billKey(b) === key) || null;
  },
  search(query) {
    const parsedQ = parseQuery(query);
    if (!parsedQ.tokens.length && !parsedQ.phrases.length) return getAllBills();
    return getAllBills().filter(b => {
      const titleLower = (b.billName || '').toLowerCase();
      const ministryLower = (b.ministryName || '').toLowerCase();
      const introducerLower = (b.billIntroducedBy || '').toLowerCase();
      const cached = state.cache.text[billKey(b)];
      const cachedLower = cached ? cached.toLowerCase() : '';
      const tokHit = t => titleLower.includes(t) || ministryLower.includes(t) || introducerLower.includes(t) || (cachedLower && cachedLower.includes(t));
      const phHit  = p => titleLower.includes(p) || ministryLower.includes(p) || introducerLower.includes(p) || (cachedLower && cachedLower.includes(p));
      return parsedQ.tokens.every(tokHit) && parsedQ.phrases.every(phHit);
    });
  },
  open(key) {
    const b = api.get(key);
    if (!b) return false;
    openBillByKey(billKey(b));
    return true;
  },
};


// ── Helpers ─────────────────────────────────────────────────────────────────

function billKey(b) {
  return `bills|${b.compositeId}`;
}

function parseDate(s) {
  if (!s) return null;
  // sansad.in dates: "1990-08-17 00:00:00.0", "2026-03-13 00:00:00.0".
  const d = new Date(String(s).replace(' ', 'T'));
  if (!isNaN(d)) return d;
  // Fallback: midnight Jan 1 of billYear.
  return null;
}

function billDate(b) {
  return parseDate(b.billIntroducedDate)
      || parseDate(b.billPassedInLSDate)
      || parseDate(b.billPassedInRSDate)
      || (b.billYear ? new Date(`Jan 1, ${b.billYear}`) : null);
}

function statusInfo(b) {
  return STATUS_INFO[b.status] || DEFAULT_STATUS;
}

function typeInfo(b) {
  return TYPE_INFO[b.billType] || DEFAULT_TYPE;
}

function houseInfo(b) {
  return HOUSE_INFO[b.billIntroducedInHouse] || { short: '?', pill: 'L' };
}

function hasExtractedText(b) {
  // manifest.texts is keyed by compositeId; absence ⇒ no text yet.
  return !!state.data.manifest?.texts?.[b.compositeId];
}

function hasSummary(b) {
  return !!state.cache.summaries[billKey(b)];
}

// ── Search query parsing (substring-only; deep-search bundle deferred) ─────

function parseQuery(raw) {
  if (!raw) return { tokens: [], phrases: [] };
  const phrases = [];
  let s = raw.replace(/"([^"]+)"/g, (_, p) => { phrases.push(p.toLowerCase().trim()); return ' '; });
  const tokens = s.split(/\s+/).filter(Boolean).map(t => t.toLowerCase());
  return { tokens, phrases };
}

function highlightMatches(text, parsedQ) {
  if (!parsedQ || (!parsedQ.tokens.length && !parsedQ.phrases.length)) return escapeHtml(text);
  const all = [...parsedQ.phrases, ...parsedQ.tokens].filter(Boolean).sort((a, b) => b.length - a.length);
  if (!all.length) return escapeHtml(text);
  const re = new RegExp(`(${all.map(t => t.replace(/[-\/\\^$*+?.()|[\]{}]/g, '\\$&')).join('|')})`, 'gi');
  return escapeHtml(text).replace(re, '<mark>$1</mark>');
}

// ── Data fetching (sharded) ────────────────────────────────────────────────

async function fetchData(forceRefresh = false) {
  const splash    = document.getElementById('splash');
  const splashErr = document.getElementById('splashError');
  const dataUrl   = _deps.config.dataBaseUrl;

  // 1. IDB cache restore (best-effort, before network)
  if (!forceRefresh) {
    try {
      const [recordsBlob, indexMetaBlob, manifestBlob, metaBlob] = await Promise.all([
        idbGet('blobs', 'bills-records.json'),
        idbGet('blobs', 'bills-index-meta.json'),
        idbGet('blobs', 'bills-manifest.json'),
        idbGet('blobs', 'bills-meta.json'),
      ]);
      if (recordsBlob && Array.isArray(recordsBlob)) {
        state.data.records   = recordsBlob;
        state.data.indexMeta = indexMetaBlob || null;
        state.data.manifest  = manifestBlob  || null;
        state.data.meta      = metaBlob      || null;
      }
    } catch (e) { console.warn('Bills IDB cache miss', e); }
  }

  const haveCache = state.data.records.length > 0;
  const bucket = Math.floor(Date.now() / 300000);
  const v = forceRefresh ? `?v=${Date.now()}` : `?v=${bucket}`;
  const fetchOpts = { cache: 'no-cache' };

  try {
    // 2. Network: fetch index-meta.json (small) first, then all shards in parallel.
    const [indexMeta, manifest, meta] = await Promise.all([
      fetch(dataUrl + CORPUS_PREFIX + 'index-meta.json' + v, fetchOpts).then(r => r.ok ? r.json() : Promise.reject(`index-meta.json: ${r.status}`)),
      fetch(dataUrl + CORPUS_PREFIX + 'manifest.json'   + v, fetchOpts).then(r => r.ok ? r.json() : { texts: {} }).catch(() => ({ texts: {} })),
      fetch(dataUrl + CORPUS_PREFIX + 'meta.json'       + v, fetchOpts).then(r => r.ok ? r.json() : null).catch(() => null),
    ]);

    const shardList = indexMeta?.shards || [];
    if (!shardList.length) {
      throw new Error('index-meta.json has no shards listed');
    }

    const shardJsons = await Promise.all(shardList.map(s =>
      fetch(dataUrl + CORPUS_PREFIX + s.file + v, fetchOpts)
        .then(r => r.ok ? r.json() : Promise.reject(`${s.file}: ${r.status}`))
    ));

    // 3. Merge shards into a flat array, in shard order (newest first across shards).
    const records = [];
    for (const s of shardJsons) {
      if (Array.isArray(s.records)) records.push(...s.records);
    }

    state.data.records   = records;
    state.data.indexMeta = indexMeta;
    state.data.manifest  = manifest;
    state.data.meta      = meta;

    // Cache under bills-prefixed IDB keys so DRSC / CAG / Bills don't collide.
    idbPut('blobs', 'bills-records.json',    records).catch(() => {});
    idbPut('blobs', 'bills-index-meta.json', indexMeta).catch(() => {});
    idbPut('blobs', 'bills-manifest.json',   manifest).catch(() => {});
    if (meta) idbPut('blobs', 'bills-meta.json', meta).catch(() => {});
  } catch (err) {
    console.error('Bills fetch failed', err);
    if (!haveCache) {
      _deps.ui.toast('Could not fetch Bills data — see console for details');
      return false;
    }
    _deps.ui.toast('Bills offline — using cached data');
  }

  splash?.classList.add('hide');
  return true;
}

async function fetchBillText(bill) {
  const key = billKey(bill);
  if (state.cache.text[key]) return state.cache.text[key];

  try {
    const cached = await idbGet('texts', key);
    if (cached) {
      state.cache.text[key] = cached;
      return cached;
    }
  } catch {}

  const entry = state.data.manifest?.texts?.[bill.compositeId];
  if (!entry) return null;
  try {
    const url = entry.url || `text/${bill.compositeId}.txt`;
    const res = await fetch(_deps.config.dataBaseUrl + CORPUS_PREFIX + url);
    if (!res.ok) return null;
    const text = await res.text();
    state.cache.text[key] = text;
    idbPut('texts', key, text).catch(() => {});
    return text;
  } catch (e) {
    console.warn('Bills: failed to fetch text', e);
    return null;
  }
}

// ── Filter / list ───────────────────────────────────────────────────────────

function getAllBills() {
  return state.data.records.map(b => ({
    ...b,
    _date: billDate(b),
    _statusInfo: statusInfo(b),
    _typeInfo: typeInfo(b),
    _houseInfo: houseInfo(b),
  }));
}

function applyFilters() {
  const all = getAllBills();
  const f = state.filters;
  const rawQ = f.search.trim();
  const parsedQ = rawQ ? parseQuery(rawQ) : null;

  const filtered = all.filter(b => {
    if (f.status && b.status !== f.status) return false;
    if (f.billType && b.billType !== f.billType) return false;
    if (f.billCategory && b.billCategory !== f.billCategory) return false;
    if (f.billYear && String(b.billYear || '') !== f.billYear) return false;
    if (f.billIntroducedInHouse && b.billIntroducedInHouse !== f.billIntroducedInHouse) return false;
    if (!parsedQ) return true;

    const titleLower = (b.billName || '').toLowerCase();
    const ministryLower = (b.ministryName || '').toLowerCase();
    const introducerLower = (b.billIntroducedBy || '').toLowerCase();
    const cached = state.cache.text[billKey(b)];
    const cachedLower = cached ? cached.toLowerCase() : '';

    function tokenHits(t) {
      return titleLower.includes(t) || ministryLower.includes(t) || introducerLower.includes(t) || (cachedLower && cachedLower.includes(t));
    }
    function phraseHits(p) {
      return titleLower.includes(p) || ministryLower.includes(p) || introducerLower.includes(p) || (cachedLower && cachedLower.includes(p));
    }
    return parsedQ.tokens.every(tokenHits) && parsedQ.phrases.every(phraseHits);
  });

  filtered.sort((a, b) => {
    switch (f.sort) {
      case 'year_asc':  return (a.billYear || 0) - (b.billYear || 0);
      case 'date_desc': return (b._date || 0) - (a._date || 0);
      case 'date_asc':  return (a._date || 0) - (b._date || 0);
      case 'year_desc':
      default:          return (b.billYear || 0) - (a.billYear || 0);
    }
  });

  state.filtered = filtered;
  renderList();
  renderResultsLine();
}

function renderResultsLine() {
  const el = document.getElementById('resultsLine');
  if (!el) return;
  const total = state.data.records.length;
  const shown = state.filtered.length;
  const meta = state.data.meta;
  const indexMeta = state.data.indexMeta;
  let metaLine = '';
  if (indexMeta?.scraped_at) {
    metaLine = `Mirror updated <b>${escapeHtml(indexMeta.scraped_at.replace('T', ' ').replace('Z', ' UTC'))}</b>`;
  } else if (meta?.last_update) {
    metaLine = `Mirror updated <b>${escapeHtml(meta.last_update.replace('T', ' ').replace('Z', ' UTC'))}</b>`;
  }
  const withText = Object.keys(state.data.manifest?.texts || {}).length;
  const indexLine = withText
    ? `<span>Title search · <b>${withText}</b> bills with text</span>`
    : '';
  el.innerHTML = `Showing <b>${shown}</b> of <b>${total}</b> bills. ${metaLine} ${indexLine}`;
}

function renderList() {
  const list = document.getElementById('reportsList');
  if (!list) return;
  if (!state.filtered.length) {
    list.innerHTML = `<div class="empty-state"><h3>No bills match</h3><p>Try clearing filters.</p></div>`;
    return;
  }
  const slice = state.filtered.slice(0, 200);
  const rawQ = state.filters.search.trim();
  const parsedQ = rawQ ? parseQuery(rawQ) : null;
  list.innerHTML = slice.map(b => {
    const titleHTML = parsedQ ? highlightMatches(b.billName || '(untitled)', parsedQ)
                              : escapeHtml(b.billName || '(untitled)');
    const hi = b._houseInfo;
    const ti = b._typeInfo;
    const si = b._statusInfo;
    const subline = [
      b.billNumber ? `Bill ${b.billNumber}` : null,
      b.ministryName,
      b.billIntroducedBy,
    ].filter(Boolean).join(' · ');
    const yearStr = b.billYear ?? '—';
    return `
      <div class="report-row" data-key="${escapeHtml(billKey(b))}">
        <div>
          <div class="report-title">${titleHTML}</div>
          <div class="report-meta">${escapeHtml(subline) || '—'}</div>
        </div>
        <span class="house-pill house-${hi.pill}" title="${escapeHtml(b.billIntroducedInHouse || '')}">${escapeHtml(hi.short)}</span>
        <span class="cat-badge cat-${ti.class}" title="${escapeHtml(b.billType || '')}">${escapeHtml(ti.short)}</span>
        <span class="cat-badge cat-${si.class}" title="${escapeHtml(b.status || '')}">${escapeHtml(si.label)}</span>
        <span class="report-meta">${escapeHtml(String(yearStr))}</span>
        <span class="text-status ${hasSummary(b) ? 'summary' : (hasExtractedText(b) ? 'ok' : '')}">
          ${hasSummary(b) ? 'summary' : (hasExtractedText(b) ? 'text' : 'metadata')}
        </span>
      </div>
    `;
  }).join('');
  if (state.filtered.length > 200) {
    list.insertAdjacentHTML('beforeend',
      `<div class="empty-state" style="padding:18px"><p>Showing first 200 of ${state.filtered.length} matches — refine filters to narrow further.</p></div>`);
  }
}


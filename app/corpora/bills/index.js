function populateFilters() {
  const records = state.data.records;
  const statusSet = new Set();
  const typeSet = new Set();
  const catSet = new Set();
  const yearSet = new Set();
  const houseSet = new Set();
  for (const b of records) {
    if (b.status) statusSet.add(b.status);
    if (b.billType) typeSet.add(b.billType);
    if (b.billCategory) catSet.add(b.billCategory);
    if (b.billYear) yearSet.add(b.billYear);
    if (b.billIntroducedInHouse) houseSet.add(b.billIntroducedInHouse);
  }

  function fill(id, values, label, sortFn) {
    const sel = document.getElementById(id);
    if (!sel) return;
    const sorted = [...values].sort(sortFn || ((a, b) => String(a).localeCompare(String(b))));
    sel.innerHTML = `<option value="">${label}</option>` +
      sorted.map(v => `<option value="${escapeHtml(String(v))}">${escapeHtml(String(v))}</option>`).join('');
  }
  fill('filterStatus',   statusSet, 'All statuses');
  fill('filterBillType', typeSet,   'All bill types');
  fill('filterBillCategory', catSet, 'All categories');
  fill('filterHouse',    houseSet,  'Both houses');
  fill('filterYear',     yearSet,   'All years', (a, b) => b - a);

  // Restore selections
  const restore = (id, key) => { const el = document.getElementById(id); if (el) el.value = state.filters[key] || ''; };
  restore('filterStatus', 'status');
  restore('filterBillType', 'billType');
  restore('filterBillCategory', 'billCategory');
  restore('filterHouse', 'billIntroducedInHouse');
  restore('filterYear', 'billYear');
}


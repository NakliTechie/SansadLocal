function openBillByKey(key) {
  const all = getAllBills();
  const b = all.find(x => billKey(x) === key);
  if (!b) return;
  if (_deps.ai.streaming() && state.streamingContext?.billKey !== key) {
    _deps.ai.stop();
    state.streamingContext = null;
  }
  state.selectedBill = b;
  state.dialogChat = state.cache.chats[key] ? [...state.cache.chats[key]] : [];
  document.getElementById('reportTitle').textContent = b.billName || '(untitled)';
  renderDetailsTab(b);
  switchReportTab('details');
  document.getElementById('reportModal').classList.add('open');
}

function _fileLink(label, url) {
  if (!url) return '';
  const filename = String(url).split('/').pop();
  return `<dt>${escapeHtml(label)}</dt><dd><a href="${escapeHtml(url)}" target="_blank" rel="noopener">${escapeHtml(filename)}</a></dd>`;
}

